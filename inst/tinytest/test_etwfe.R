expect_equal(fixest::coeftable(m1a), m1_known)
expect_equal(fixest::coeftable(m1r), m1_known)

# With never-treated control group
m2 = etwfe(
  lemp ~ 0,
  tvar = year,
  gvar = first.treat,
  data = mpdta,
  vcov = ~countyreal,
  cgroup = "never"

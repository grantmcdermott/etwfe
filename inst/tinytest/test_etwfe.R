# No controls
m1 = etwfe(
  lemp ~ 0,
  tvar = year,
  gvar = first.treat,
  data = mpdta,
  vcov = ~countyreal
)
m1a = etwfe(
  lemp ~ 0,
  tvar = "year",
  gvar = "first.treat",
  data = mpdta,
  vcov = ~countyreal
) # chars instead of nse
m1r = etwfe(
  lemp ~ 0,
  tvar = "year",
  gvar = "first.treat",
  data = mpdta,
  vcov = ~countyreal,
  gref = 0
) # with explicit ref

